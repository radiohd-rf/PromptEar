"""Определение темы текста по ключевым словам."""

TOPIC_KEYWORDS = {
    "история": [
        "история",
        "век",
        "император",
        "война",
        "революция",
        "князь",
        "царь",
        "битва",
        "государство",
        "полководец",
        "империя",
        "древний",
        "средневековье",
        "поход",
        "династия",
    ],
    "образование": [
        "лекция",
        "студент",
        "университет",
        "экзамен",
        "курс",
        "обучение",
        "теория",
        "преподаватель",
        "семинар",
        "диплом",
    ],
    "техника": [
        "код",
        "сервер",
        "алгоритм",
        "нейросеть",
        "программа",
        "данные",
        "система",
        "модель",
        "чип",
        "процессор",
    ],
    "повседневный": [],
}


def detect_topic(text: str) -> str:
    """Определяет тему текста по ключевым словам.

    Возвращает одну из: история, образование, техника, повседневный.
    """
    text_lower = text.lower()
    scores = {}
    for topic, keywords in TOPIC_KEYWORDS.items():
        if not keywords:
            continue
        scores[topic] = sum(1 for kw in keywords if kw in text_lower)

    if not scores:
        return "повседневный"

    best = max(scores, key=lambda k: scores[k])
    return best if scores[best] > 0 else "повседневный"
